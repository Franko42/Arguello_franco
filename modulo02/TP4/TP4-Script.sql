 Codigo_Localidad INT  PRIMARY KEY  NOT NULL,
 Nombre VARCHAR(45) ,
 Codigo_Postal INT ,
 PROVINCIA_ID_Provincia INT NOT NULL,
 EMPLEADO_id_Empleado INT NOT NULL
 
);
    

