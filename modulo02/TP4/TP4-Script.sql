  id_Empleado  INT PRIMARY KEY NOT NULL,
  DNI INT ,
  Nombre VARCHAR(45),
  Telefono VARCHAR(45) ,
  Email VARCHAR(45) ,
  Fecha_alta DATE 
  
);

